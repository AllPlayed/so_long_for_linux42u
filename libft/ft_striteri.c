#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int		c;

	c = 0;
	if (!s || !f)
		return ;
	while (s[c] != '\0')
	{
		f(c, &s[c]);
		c++;
	}
}
